async function main() {
  await prisma.orderItem.deleteMany();
  await prisma.order.deleteMany();
  await prisma.cartItem.deleteMany();
  await prisma.pizzaSize.deleteMany();
  await prisma.pizza.deleteMany();
  await prisma.user.deleteMany();

  const adminPassword = await bcrypt.hash("admin12345", 10);
  const userPassword = await bcrypt.hash("user12345", 10);

  await prisma.user.create({
    data: {
      username: "admin",
      email: "admin@pizza.com",
      password: adminPassword,
      role: Role.ADMIN,
    },
  });

  await prisma.user.create({
    data: {
      username: "user",
      email: "user@pizza.com",
      password: userPassword,
      role: Role.USER,
    },
  });

  for (const [name, description, category, basePrice] of pizzaCatalog) {
    await prisma.pizza.create({
      data: {
        name,
        description,
        category,
        imageUrl: null,
        sizes: {
          create: [
            { size: PizzaSizeType.SMALL, diameterCm: 25, price: basePrice },
            { size: PizzaSizeType.MEDIUM, diameterCm: 30, price: basePrice + 4 },
            { size: PizzaSizeType.LARGE, diameterCm: 35, price: basePrice + 8 }
          ],
        },
      },
    });
  }

  console.log("Seed completed");
  console.log("Admin: admin@pizza.com / admin12345");
  console.log("User: user@pizza.com / user12345");
  console.log("Pizzas created:", pizzaCatalog.length);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
